# Returns lines of the form:
#  APP_NAME PROJECT_NAME DOCKER_COMPOSE_FILE

for APP_NAME_PART_1 in $(ls $APP_DIR); do
  for APP_NAME_PART_2 in $(ls $APP_DIR/$APP_NAME_PART_1); do
    APP_NAME=$APP_NAME_PART_1/$APP_NAME_PART_2
    PROJECT_NAME=$APP_NAME_PART_1
    DOCKER_COMPOSE_FILE=$APP_DIR/$APP_NAME/docker-compose.yml
    ROOT_PATH=$APP_DIR/$APP_NAME
    echo $APP_NAME $PROJECT_NAME $DOCKER_COMPOSE_FILE $ROOT_PATH
  done
done
